// 创建一个“Hello World!”使用C++

// We call the header file library
#include <iostream>

// We call the main Function and the things inside the {} will be executed by the name of the Main() function
int main() {
    // With cout << we call that that line will print something
    std::cout << "Hello World!";
    // End of the function
    return 0;
}

// If we wanna put two codes one below other we should use \n\n
int main() {
    std::cout << "Hello World! \n\n";
    std::cout << "I am learning C++";
    return 0;
}
